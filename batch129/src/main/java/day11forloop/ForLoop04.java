package day11forloop;

public class ForLoop04 {
    public static void main(String[] args) {
        //Example : 5 ten 8 e kadar tamsayilarin toplamini veren kodu yaziniz.
        int a=0;
        for (int i=5;i<9;i++){
            a=a+i;
        }
        System.out.println(a);
        //Example: 7 den 9 a kadar tamsayilarin carpimin yapan kodu yaziniz.
        int b=1;
        for (int i=7;i<10;i++){
            b=b*i;
        }
        System.out.println(b);







    }
}
