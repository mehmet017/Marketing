package day11forloop;

public class ForLoop05 {
    public static void main(String[] args) {
        for (int i=1;i<4;i++){
            System.out.println("Week: "+i);
           for (int y=1;y<4;y++){
               System.out.println("Day: "+y);
           }
            System.out.println("....");
        }



    }
}
