public class Q01_Ascii_AdiniYaz {
    //Adinizin icerdigi hareleri ascii table bakmadan, adinizi yazdiran bir kod yaziniz//"Ali"
    public static void main(String[] args) {

        char ch1='B'-1;//A
        char ch2='m'-1;//l
        char ch3='h'+1;//i

        System.out.println(""+ch1+ch2+ch3);//Ali




    }

}
